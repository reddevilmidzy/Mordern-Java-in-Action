package src.main.java.modernjavainaction.ch01;

public class Apple {

    private Integer wight;
    private Color color;

    public Apple(Integer wight, Color color) {
        this.wight = wight;
        this.color = color;
    }

    public Integer getWight() {
        return wight;
    }

    public Color getColor() {
        return color;
    }
}
